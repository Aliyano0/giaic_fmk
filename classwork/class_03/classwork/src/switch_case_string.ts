// Example 2: Using switch case with strings
let color: string = "red";
switch (color) {
  case "red":
    console.log("The color is red");
    break;
  case "green":
    console.log("The color is green");
    break;
  case "blue":
    console.log("The color is blue");
    break;
  default:
    console.log("The color is unknown");
    break;
}
