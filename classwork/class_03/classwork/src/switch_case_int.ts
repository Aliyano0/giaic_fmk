// Example 1: Using switch case with integers
let score: number = 100;
switch (score) {
  case 100:
    console.log("Perfect score");
    break;
  case 90:
    console.log("Excellent score");
    break;
  case 80:
    console.log("Good score");
    break;
  case 70:
    console.log("Passing score");
    break;
  default:
    console.log("Failing score");
    break;
}
